			<h2>Frequently Asked Questions</h2>
			<h4>What is this?</h4>
			<p>Have you ever debated with your friends over what the best pizza topping or the greatest Pixar movie is? Well this is here to provide a totally definitive ranking to solve these debates once and for all, using the power of crowdsourcing!</p>
			<h4>How do I take part?</h4>
			<p>Each week, there will be a category such as "colors" or "sports mascots", and you will be presented with a series of this-or-that votes in which you have to decide which you like more or which is better. These votes will be combined to form a total rating, which gets updated throughout the week. It's that simple!</p>
			<h4>What if I don't know about something?</h4>
			<p>A lot of times, there'll be links for more information if you're so inclined. If you're really stuck, just press the button that says you don't know enough to make a decision, and that matchup will be skipped. But what's the fun in that? Go with your gut instinct! No one said there had to be rigerous criteria on how to decide.</p>
			<h4>Why can I only vote so many times?</h4>
			<p>I don't want one person mindlessly clicking for an hour to skew the results. I want to give people plenty of chances to vote, since the more data the better the rankings will be, but ideally I'd like everyone to hold more or less the same amount of sway over the final results.</p>
			<h4>How often are these votes done?</h4>
			<p>A new poll will get released every Monday at 12 noon Eastern time.</p>
			<h4>What do the ratings mean?</h4>
			<p>Ratings are calulated using Maximum Likelihood (details coming soon), and adjusted for human readability. Essentially, a score of 5 is "average", and the higher the better. In theory it's possible to have a negative rating or one over 10, but they would be hard to achieve (and values of 0 and 10 don't actually have any meaning anyway).</p>
			<h4>Why isn't this or that thing in your ranking?</h4>
			<p>I might exclude entries for several reasons, such as being too obscure, there being too many things already, or if it's something that I'd not feel comfortable putting in. Alternatively, I might have just forgotten.</p>
